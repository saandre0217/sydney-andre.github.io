// drawChessboard //////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function drawChessboard(x) { //needs param
var string = '';
for(var i = 0; i < x; i++){
  for(var j = 0; j < x; j++){
    if((i + j) % 2 === 0){
      string += ' ';
    } else {
      string += '#';
    }
  }
  string += '\n';
}
console.log(string);
}

