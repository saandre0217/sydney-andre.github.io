// fizzBuzz ////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function fizzBuzz() {
    //use for loop start at 1, end at 15, increment
    //if statement withing for loop using % 
    for (var i = 1; i <= 15; i++) {
        if (i % 3 === 0 && i % 5 === 0) {
            console.log('fizzbuzz')
        } else if (i % 3 === 0) {
            console.log('fizz')
        } else if (i % 5 === 0) {
            console.log("buzz")
        } else {
            console.log(i)
        }
        
    }
  
}

