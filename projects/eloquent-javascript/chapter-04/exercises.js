function range(num1, num2, step) { //HW
  let results = [];
  if(num1 === num2){
    return [];
  }

  if (!step){
  for(let i = num1; i <= num2; i++){
    results.push(i);
  }
  return results;
 } else if(step > 0){
  for(let i = num1; i <= num2; i+=step){
    results.push(i);
 }
 return results;
} else if(step < 0){
  return [];
}
}



