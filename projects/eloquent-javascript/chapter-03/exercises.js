////////////////////////////////////////////////////////////////////////////////
// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(x, y) {
if(x > y){
  return y;
} else if(x < y){
  return x;
} else {
  return x, y;
}
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(x) {
  if(x % 2 === 0){
    return true;
  } else if(x % 2 !== 0){
    return false;
  }

}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, letter) {
  let count = 0;
  for(let i = 0; i < string.length; i++){
    if(string.charAt(i) === letter){
      count += 1;
    }
  }
  return count;
}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
let count = 0;
for(let i = 0; i < string.length; i++){
  if(string.charAt(i) === 'B'){
    count += 1;
  }
}
return count;
}

